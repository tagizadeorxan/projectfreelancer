    private void addnewFreelancer() {
     try {
       Date date = new Date ();
       jDateChooser1.setDate(date);
         Freelancer freelancer = new Freelancer();
         freelancer.setName(jTextField1.getText());
         freelancer.setSurname(jTextField2.getText());
         freelancer.setEmailAddress(jTextField3.getText());
         freelancer.setPhoneNumber(jTextField4.getText());
         freelancer.setCity(jTextField5.getText());         
         java.sql.Date dob = new java.sql.Date(jDateChooser1.getDate().getTime());
         freelancer.setDateofBirth(dob);
         freelancer.setPhoto(jTextField7.getText());
         freelancer.setPhotoIDcard(jTextField8.getText());
         freelancer.setPhotowithIDcard(jTextField9.getText());
         freelancer.setAbout(jTextField10.getText());
         freelancer.setExperience(jTextField11.getText());
         freelancer.setLinktoFacebook(jTextField12.getText());
         freelancer.setLinktoSkype(jTextField13.getText());
         FreelancerDao freelancerdao = new FreelancerDaoImpl();
         FreelancerService freelancers = new FreelancerServiceImpl(freelancerdao);
        boolean isAdded =  freelancers.addNewFreelancer(freelancer);
        if (isAdded) {
            JOptionPane.showMessageDialog(rootPane, "Added");
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Not Added","Error",JOptionPane.ERROR_MESSAGE);
        }
     } catch (Exception ex) {
ex.printStackTrace();
     }
    }

    
    
     
         
     
          
             
          
    
